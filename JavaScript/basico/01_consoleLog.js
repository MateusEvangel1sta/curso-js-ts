// Metódo utilizado para imprimir dados no console.
/**
 * Nos exemplos abaixos, estamos exbibindo STRINGS (textos).
 * Podemos exibir strings com aspas simples, aspas duplas e crases.
 * Se iniciarmos uma STRING com aspas simples, devemos fechá-la da mesma forma.
 * Podemos usar aspas duplas dentro de aspas simples para destacar alguma caractere/palavra.
 */
console.log('Olá, Deus!');
console.log("Olá, Deus!");
console.log(`Olá, Deus!`);

console.log('"Olá", Deus!');
console.log(`"Este" 'é' "mais" 'um' "exemplo".`);

// Podemos exibir números inteiro e decimais. Ambos os tipos são parte do tipo NUMBER
// int - Número inteiro
console.log(123456);

// float -> Número decimal
console.log(15.86);

// Exibindo tipos
// Para exibirmos os tipos, basta utilizar o typeof(). O uso dos parênteses são opciosnais.
console.log(typeof 'Olá, Deus!', typeof(123456), typeof 15.86);